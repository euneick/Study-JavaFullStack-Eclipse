
/**
 * 문자 형태의 자료형 저장하기
 */
public class Data07 {

	public static void main(String[] args) {

		char c = 'A';
		System.out.printf("%c -> %d\n", c, (int) c);

		c = '0';
		System.out.printf("%c -> %d\n", c, (int) c);
		
		c = 0;
		System.out.printf("%c -> %d\n", c, (int) c);
		
		c = 'a';
		System.out.printf("%c -> %d\n", c, (int) c);
	}

}
