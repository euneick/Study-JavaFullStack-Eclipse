
/**
 * 변수의 값을 1증가 시키는 증가연산자 ++
 */
public class Opr07 {

	public static void main(String[] args) {

		int a = 10, b = 10;

		++a;
		System.out.println(a);

		b++;
		System.out.println(b);

		int c = 1;
		System.out.println(c++);
		System.out.println(c);
		
		System.out.println(++c);
		System.out.println(c);
	}

}
